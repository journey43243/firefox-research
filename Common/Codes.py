# -*- coding: utf-8 -*-
"""
Модуль кодов выхода приложения

Этот модуль определяет перечисление (enum) с кодами выхода приложения.
Каждый код соответствует определённому типу ошибки или успешному завершению.
Коды используются для информирования операционной системы о результате 
выполнения программы.
"""

from enum import IntEnum

# ################################################################
# Перечисление кодов выхода приложения
# ################################################################
class ExitCode(IntEnum):
    """
    Перечисление кодов выхода приложения.
    
    Коды выхода:
        Ok (0): Приложение завершилось успешно без ошибок
        AsyncStartError (1): Ошибка при запуске или выполнении асинхронного цикла событий
        ControlParametersError (2): Ошибка в параметрах управления приложением
        InputParametersError (3): Ошибка в входных параметрах (аргументы командной строки)
    """
    Ok = 0  # Успешное завершение
    AsyncStartError = 1  # Ошибка асинхронного запуска
    ControlParametersError = 2  # Ошибка параметров управления
    InputParametersError = 3  # Ошибка входных параметров